package com.Aurionpro.Operators;

public class Assignment {
	public static void main(String[] args) {
		System.out.println("ASSIGNMENT OPERATORS");
		int num1, num2=20;
 
        // Assigning values
        num1 = 10; 
        // Displaying the assigned values
        System.out.println("num is assigned: " + num1);
        
        System.out.println("\nnum1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("Adding & Assigning values"); 
        num1 += num2;
        // Displaying the assigned values
        System.out.println("num1 = " + num1);
	
        System.out.println("\n\nnum1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("Subtracting & Assigning values"); 
        num1 -= num2;
        // Displaying the assigned values
        System.out.println("num1 = " + num1);
    
        System.out.println("\n\nnum1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("Multiplying & Assigning values"); 
        num1 *= num2;
        // Displaying the assigned values
        System.out.println("num1 = " + num1);
        
        System.out.println("\n\nnum1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("Dividing & Assigning values"); 
        num1 /= num2;
        // Displaying the assigned values
        System.out.println("num1 = " + num1);
	
        System.out.println("\n\nnum1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("Moduling & Assigning values"); 
        num1 %= num2;
        // Displaying the assigned values
        System.out.println("num1 = " + num1);
	}
}
